package com.zoho.studentmanagement;

public class StudentDepartment {
	private String studentDepartmentName;
	private String studentCourseName;
	public String getStudentDepartmentName() {
		return studentDepartmentName;
	}
	public void setStudentDepartmentName(String studentDepartmentName) {
		this.studentDepartmentName = studentDepartmentName;
	}
	public String getStudentCourseName() {
		return studentCourseName;
	}
	public void setStudentCourseName(String studentCourseName) {
		this.studentCourseName = studentCourseName;
	}
	
	

}
